package lesson2.primitives;

public class CharClass {
    public static void main(String[] args) {
        //char
        char myCharUpperCase = 'W';
        char myCharLowerCase = 'w';
        System.out.println(myCharUpperCase);
        System.out.println(myCharLowerCase);
        System.out.println("=========");
        int myCharUpperCaseInteger = myCharUpperCase;
        System.out.println(myCharUpperCaseInteger);
        int myCharLowerCaseInteger = myCharLowerCase;
        System.out.println(myCharLowerCaseInteger);
        System.out.println("========");
        char myChar = 87;
        System.out.println(myChar);
    }
}
